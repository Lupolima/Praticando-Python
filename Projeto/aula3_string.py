#python ja entende que é um numero
print(1234)

# Aspas simples funciona como String normal
print('Lucas Guilherme')

# Aspaas duplas funciona como String normal
print("Lucas Guilherme")

# Caractere de Escape, para pulhar o proximo caractere após ela
print("Lucas \"Guilherme\"")

# r
print(r"Lucas \"Guilherme\"")

#Melhor utilizar assim:
