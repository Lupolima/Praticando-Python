# A função 'input' recebe o que você digita no teclado e guarda na variável 'nome'
nome = input("Digite seu nome: ")

# Aqui exibimos uma mensagem de boas-vindas usando uma 'f-string' para incluir a variável
print(f"Olá, {nome}! Seu ambiente Python no Pop!_OS está funcionando.")

# Criamos duas variáveis numéricas para um teste matemático simples
numero_1 = 10
numero_2 = 5

# Realizamos a soma e guardamos o resultado em uma nova variável
soma = numero_1 + numero_2

# Exibimos o resultado do cálculo no terminal do VS Code
print(f"O resultado da soma de {numero_1} com {numero_2} é: {soma}")